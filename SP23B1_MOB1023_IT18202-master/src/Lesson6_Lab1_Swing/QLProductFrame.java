    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Product products = getData();
        if (products == null) {
            return;
        }
        iProduct_Service.Insert(products);
        loadTable();
        clear();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        Product products = getData();
        if (products == null) {
            return;
        }
        int row = tblTB.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để sửa");
            return;
        }
        iProduct_Service.Update(row, products);
        loadTable();
        clear();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblTB.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để xóa");
            return;
        }
        iProduct_Service.Delete(row);
        loadTable();
        clear();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTBMouseClicked
        int row = tblTB.getSelectedRow();
        if (row == -1) {
            return;
        }
        txtName.setText(tblTB.getValueAt(row, 0).toString());
        txtPrice.setText(tblTB.getValueAt(row, 1).toString());
    }//GEN-LAST:event_tblTBMouseClicked

