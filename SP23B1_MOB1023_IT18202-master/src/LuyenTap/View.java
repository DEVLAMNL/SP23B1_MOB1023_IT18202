    public ArrayList<People> fillTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblTB.getModel();
        dtm.setRowCount(0);
        for (People people : isv.getlist()) {
            dtm.addRow(new Object[]{
                people.getHoTen(), people.getDiaChi(), people.Gioitinh(),});
        }
        return list;
    }

    public People getData() {
        String hoTen = "";
        try {
            hoTen = txtTen.getText();
            if (hoTen.isBlank()) {
                JOptionPane.showMessageDialog(null, "Dữ Liệu Trống");
                return null;
            }
        } catch (Exception e) {
        }
        String diaChi = txtDiaChi.getText();

        if (diaChi.isBlank()) {
            JOptionPane.showMessageDialog(null, "Dữ Liệu Trống");
            return null;
        }

        boolean gioiTinh = true;

        gioiTinh = rdoNam.isSelected();

        People pl = new People(hoTen, diaChi, gioiTinh);
        return pl;
    }

